package movies.domain;

public class Movies {
    private String name;

    public Movies() {
    }

    public Movies(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return this.name;
    }
}
